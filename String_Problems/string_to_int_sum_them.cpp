#include<bits/stdc++.h>  // সব স্ট্যান্ডার্ড লাইব্রেরি ইনক্লুড করে নেয়
using namespace std;     // std:: না লিখে সরাসরি cout, string ইত্যাদি ব্যবহার করার জন্য

int main ()
{
    string s;                // ইনপুট স্ট্রিং রাখার জন্য ভেরিয়েবল
    getline(cin, s);        // ইউজারের কাছ থেকে পুরো লাইন ইনপুট নেয় (স্পেস সহ)

    int n = s.length();     // স্ট্রিং-এর দৈর্ঘ্য (length) বের করা হচ্ছে
    int sum = 0;            // সব নাম্বারের যোগফল রাখার জন্য ভেরিয়েবল
    string temp = "";       // সংখ্যাগুলো আলাদা করে জমিয়ে রাখার জন্য টেম্পোরারি স্ট্রিং

    // স্ট্রিং-এর প্রতিটি ক্যারেক্টার একে একে চেক করার জন্য লুপ
    for(int i = 0; i < n; i++)
    {
        char ch = s[i];          // স্ট্রিং-এর i তম ক্যারেক্টার নিচ্ছি

        if(isdigit(ch))          // যদি ক্যারেক্টারটা সংখ্যা হয় (0–9)
        {
            temp = temp + ch;    // সেই সংখ্যা temp-এ জমিয়ে রাখছি
        }
        else if(!temp.empty())   // যদি সংখ্যা শেষ হয় এবং temp ফাঁকা না থাকে
        {
            sum += stoi(temp);   // temp স্ট্রিংকে নাম্বারে রূপান্তর করে sum-এ যোগ করছি
            temp = "";           // temp খালি করে দিচ্ছি পরের সংখ্যার জন্য
        }
    }

    // যদি স্ট্রিং শেষ হয় এবং শেষে নাম্বার থেকে যায় (যেমন: "abc123")
    if(!temp.empty())
    {
        sum += stoi(temp);   // সেটাও যোগ করছি
        temp = "";           // খালি করে দিচ্ছি
    }

    cout << sum << endl;     // সব সংখ্যার যোগফল প্রিন্ট করছি

    return 0;                // প্রোগ্রাম সফলভাবে শেষ
}
